package main

import "fmt"

func mathOperators() {
	// a, b := 10, 3 // Shorthand declaration for multiple variables

	a := 10
	b := 3

	fmt.Println(a + b) // 13
	fmt.Println(a - b) // 7
	fmt.Println(a * b) // 30
	fmt.Println(a / b) // 3
	fmt.Println(a % b) // 1 -> module

	fmt.Println(5 / 2)     // 2
	fmt.Println(5.0 / 2.0) // 2.5
}
